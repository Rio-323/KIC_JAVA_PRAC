public class VariableEx07 {
    public static void main(String[] args) {
        // 2.5
        // int i = 2.5;
        // System.out.println(i);

        // 모든 실수의 기본형은 double
        float f = 2.5f;
        System.out.println(f);

        double d = 2.5;
        System.out.println(d);

        // 지수
        double d1 = 1.0e3;
        System.out.println(d1);
    }
}
