package player;

public class SoccerDTO {
	private String playerNo;
	private String teamCode;
	private String playerName;
	private String playerHeight;
	private String playerBirth;
	private String playerWeight;
	private String playerPosition;
	private String playerBackNo;
	
	public SoccerDTO() { }

	public String getPlayerNo() {
		return playerNo;
	}

	public void setPlayerNo(String playerNo) {
		this.playerNo = playerNo;
	}

	public String getTeamCode() {
		return teamCode;
	}

	public void setTeamCode(String teamCode) {
		this.teamCode = teamCode;
	}

	public String getPlayerName() {
		return playerName;
	}

	public void setPlayerName(String playerName) {
		this.playerName = playerName;
	}

	public String getPlayerHeight() {
		return playerHeight;
	}

	public void setPlayerHeight(String playerHeight) {
		this.playerHeight = playerHeight;
	}

	public String getPlayerBirth() {
		return playerBirth;
	}

	public void setPlayerBirth(String playerBirth) {
		this.playerBirth = playerBirth;
	}

	public String getPlayerWeight() {
		return playerWeight;
	}

	public void setPlayerWeight(String playerWeight) {
		this.playerWeight = playerWeight;
	}

	public String getPlayerPosition() {
		return playerPosition;
	}

	public void setPlayerPosition(String playerPosition) {
		this.playerPosition = playerPosition;
	}

	public String getPlayerBackNo() {
		return playerBackNo;
	}

	public void setPlayerBackNo(String playerBackNo) {
		this.playerBackNo = playerBackNo;
	}
}
